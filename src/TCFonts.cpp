void TCFonts::initFont( uint8_t fontIndex, std::map<String, std::vector<uint8_t>>& font ) {
  if( fontIndex == 0 ) {
    TCFonts::initFont0( font );
  } else if( fontIndex == 1 ) {
    TCFonts::initFont1( font );
  } else if( fontIndex == 2 ) {
    TCFonts::initFont2( font );
  } else {
    TCFonts::initFont0( font );
  }
}

std::map<String, std::vector<uint8_t>> TCFonts::getFont( uint8_t fontIndex ) {
  if( TCFonts::fontIndex == fontIndex ) {
    return TCFonts::symbols;
  }
  std::map<String, std::vector<uint8_t>> font;
  TCFonts::initFont( fontIndex, font );
  return font;
}

void TCFonts::setFont( uint8_t fontIndex ) {
  if( TCFonts::isFontInitialised && TCFonts::fontIndex == fontIndex ) return;
  TCFonts::isFontInitialised = true;
  TCFonts::fontIndex = fontIndex;
  initFont( fontIndex, TCFonts::symbols );
}

std::vector<uint8_t> TCFonts::getSymbol( std::map<String, std::vector<uint8_t>> font, char symbol, bool isBold, bool isWide, bool isSmall ) {
  String key;
  if( isSmall ) {
    //symbol
    key = String( symbol );
  } else {
    //symbol - isThin - isBold
    key = String( symbol ) + String( !isWide ? "1" : "0" ) + String( isBold ? "1" : "0" );
  }
  std::map<String, std::vector<uint8_t>>::iterator it = font.find( key );
  if( it == font.end() ) {
    return { 0, 0, 0, 0, 0, 0, 0, 0 };
  }
  return it->second;
}
